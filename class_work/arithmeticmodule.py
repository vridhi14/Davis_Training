def calculateSum(x,y):
    return(x+y)

def calculateSub(x,y):
    return(x-y)

def calculateMultiply(x,y):
    return(x*y)

def calculateDivision(x,y):
    return(x/y)

def rect_area(length, width):
    return length * width

def rect_perimeter(length, width):
    return 2 * (length + width)

def square_area(side):
    return side * side

def square_perimeter(side):
    return 4 * side
